# Simple program to print numbers from 1 to 5 using a for loop

for i in range(1, 6):  # range(1, 6) generates numbers from 1 to 5
    print(f" {i}. Good Morning")
# Simple program to print numbers from 1 to 5 using a while loop

count = 1
while count <= 5:  # Loop will run as long as count is less than or equal to 5
    print("Number:", count)
    count += 1  # Increment the count by 1
